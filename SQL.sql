SELECT COUNT(*),
YEAR(FECHA),
MONTH(FECHA),
CASE MONTH(FECHA)
WHEN 1 THEN IF(COUNT(*)=31,"OK", "ERROR")
WHEN 2 THEN IF(COUNT(*)>=28,"OK", "ERROR")
WHEN 3 THEN IF(COUNT(*)=31,"OK", "ERROR")
WHEN 4 THEN IF(COUNT(*)=30,"OK", "ERROR")
WHEN 5 THEN IF(COUNT(*)=31,"OK", "ERROR")
WHEN 6 THEN IF(COUNT(*)=30,"OK", "ERROR")
WHEN 7 THEN IF(COUNT(*)=31,"OK", "ERROR")
WHEN 8 THEN IF(COUNT(*)=31,"OK", "ERROR")
WHEN 9 THEN IF(COUNT(*)=30,"OK", "ERROR")
WHEN 10 THEN IF(COUNT(*)=31,"OK", "ERROR")
WHEN 11 THEN IF(COUNT(*)=30,"OK", "ERROR")
WHEN 12 THEN IF(COUNT(*)=31,"OK", "ERROR")
END AS X
FROM CLIMA_DIA 
GROUP BY YEAR(FECHA), MONTH(FECHA)
ORDER BY 2,3

